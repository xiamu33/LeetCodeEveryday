/**
 * @param {number} num
 * @return {string}
 */
var intToRoman = function(num) {
  const digits = [1000, 900, 500, 400, 100, 90, 50, 40, 10, 9, 5, 4, 1];
  const symbols = ['M', 'CM', 'D', 'CD', 'C', 'XC', 'L', 'XL', 'X', 'IX', 'V', 'IV', 'I'];
  let str = '';
  for (let i = 0; i < digits.length; i++) {
    while (num >= digits[i]) {
      num -= digits[i];
      str += symbols[i];
    }
  }
  return str;
};

